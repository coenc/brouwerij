<p>This web application was developed with the Laravel MVC framework and various Javascript libraries. It is a project that developed in a process of learning the Laravel framework and to show the newly acquired skills.</p>
<p>It is a production and manufucturing management tool for manufacturers that buy commodities for producing products, like beer breweries, bakeries, etc. All users share the same database, but are unaware of each others co-existence. Each user owns his own set of data.</p>

<p>FUNCTIONALITY</p>
<p>Recipie management</p>
<p>Commodities purchasing</p>
<p>Production planning</p>
<p>Stock information, based on production and purchase data.</p>
<p>Management information and graphs</p>
<p>CRM</p>
<p>....</p>
<p>....</p>

</p>

<p><b>DEMO USER LOGINS</b></p>
<p>You can login using the following user credentials:</p>
<li><b>brewer@brewery.com</b> and password '<b>brewer</b>' for an already setup brewery configuration</li>
<li><b>baker@bakery.com</b> and password '<b>baker</b>' for an already setup bakery configuration</li>
<p>Or simply create a new account to start with a blank configuration</p>

<p><b>SECURITY</b></p>
<p>All data is secured by the login system, ensuring nobody can access the application data.<br>
Secured against XSS Cross Site Scripting and code injection/SQL inhection.<br>
Passwords are stored in encrypted format.<br>
The web application is hosted on Amazon Webs Services</p>

<p><strong>HOSTING</strong></p>
<p>The application is hosted on the Amazon AWS Free Tier scheme. See <a target="_blank" href="https://aws.amazon.com/free">http://aws.amazon.com/free</a> for more details.</p>

<p><strong>Javascript libraries</strong></p>
<p>Besides the Laravel framework, the following Javascript libraries have been used:
	<li>jQuery for a interactive user-interface</li>
	<li>Bootstrap for responsive UX</li>
	<li>SweetAlert for displaying messages and confirmations</li>
	<li>FancyBox to display clickable images </li>
	<li>ParsleyJS for form data validation</li>
	<li>htmlPurifier for protection against code injection</li>
	<li>jsDataTables for sorting, paginating and ordering of html tables</li>
	<li>Select2 for a type-as-you-go listboxes</li>
	<li>Bootstrap DatePicker for calender style date input fields</li>
</p>

